const int mxn = 200005;
int a[mxn],bits[31];

int n,m,q;
mint res[mxn], fac[mxn], ifac[mxn];
void pre(int n){
    fac[0] = mint(1);
    fac[1] = mint(1);
    for (int i=2; i<=n;++i)
        fac[i] = fac[i-1]*i;
    ifac[n] = fac[n].pow(mod-2);
    for (int i=n-1; i>=0;--i)
        ifac[i] = (mint)(i+1)*ifac[i+1];
}

mint ncr(int n, int r){
    return fac[n]*ifac[r]*ifac[n-r];
}

vector<mint> mult(const vector<mint>&a, const vector<mint>& b){    vector<int> A, B;
    for(auto x: a) A.push_back(x.value);
    for(auto x: b) B.push_back(x.value);
    auto res = multiply(A, B, (A == B));
    vector<mint> ans;
    for(auto x: res) ans.push_back(mint(x));
    return ans;
}

void solve(){
    cin>>n;
    pre(n);
    int x;
    memset(bits, 0, sizeof bits);
    for(int i = 0; i<n; ++i){
        cin>>a[i];
        for(int j =0; j<31; ++j)
            bits[j]+=(a[i]>>j)&1;
    }
    std::vector<std::vector<mint>> polynomials(31);
    for(int i = 0; i<31; ++i){
        int ones = bits[i];
        int zeros = n-ones;
        vector<mint> aa(ones+1,0),bb(zeros+1,0);
        for(int j = 0; j<=ones; j++)
            aa[j]=(j%2)?ncr(ones,j):0;
        for(int j = 0; j<=zeros; j++)
            bb[j] = ncr(zeros,j);
        polynomials[i] = mult(aa,bb);
        // print(polynomials[i]);
    }
    res[0] = 0;
    for(int i = 1; i<=n; ++i){
        res[i] = res[i-1];
        for (int j = 0; j<31; ++j)
            res[i] =res[i]+ mint(2).pow(j)*polynomials[j][i];
        // cout<<res[i]<<" ";
    }
    cin>>q;
    while(q-->0){
        cin>>m;
        cout<<res[m]<<"\n";
    }
}

